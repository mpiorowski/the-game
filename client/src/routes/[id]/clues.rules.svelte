<h3 class="text-center mb-6">
    This is the most important part of the game - creating guess words.
</h3>
<ul class="list-disc text-lg mb-4">
    <li>
        Each of You will need to create 3 sentences, each containing maximum 5
        words.
    </li>
    <li>
        The first one will be a <b>movie, a book, a TV show, anime, etc.</b>
        - anything that has a title and exists in the real world.
    </li>
    <li>
        The second one will be a <b>person</b>
        , and the third one will be a
        <b>place</b>
        .
    </li>
    <li>And here is where the fun begins...</li>
    <li>
        For the <b>person</b>
        and
        <b>place</b>
        this a moment where You go nuts with the word formation. The person and place
        don't need to be real, and the sentences don't need to make any sense.
        You can even use the same word multiple times.
    </li>
    <li>
        The only rules are:
        <ul class="list-disc pl-8">
            <li>real, existing words</li>
            <li>grammatically correct</li>
            <li>no more then 5 words</li>
            <li>single language</li>
        </ul>
    </li>
</ul>
