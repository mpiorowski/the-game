<h3 class="mb-4">Tips for a better gameplay</h3>
<ul class="list-disc text-lg mb-4">
    <li>Don't use technical words, like "Cummingtonite"</li>
    <li>Try to use all 5 words for place and person</li>
    <li>
        And the most important, before You submit them...
        <b>Remember them!</b>
        You will not be able to check them again.
    </li>
</ul>
